-- PASO 5: Creación de la tabla de publicaciones (posts)
CREATE TABLE public.posts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  image_url TEXT NOT NULL,
  description TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

COMMENT ON TABLE public.posts IS 'Contiene todas las publicaciones de los usuarios.';

-- PASO 6: Habilitar RLS y definir Políticas para posts
ALTER TABLE public.posts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Posts are viewable by everyone."
ON public.posts FOR SELECT
USING (true);

CREATE POLICY "Authenticated users can create posts."
ON public.posts FOR INSERT
WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can delete their own posts."
ON public.posts FOR DELETE
USING (auth.uid() = user_id);

----------------------------------------------------------------------------------------------

-- PASO 7: Creación de la tabla de "Me Gusta" (likes)
CREATE TABLE public.likes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_id UUID REFERENCES public.posts(id) ON DELETE CASCADE NOT NULL,
  user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  -- La combinación de post_id y user_id debe ser única para evitar que un usuario dé like dos veces.
  UNIQUE(post_id, user_id)
);

COMMENT ON TABLE public.likes IS 'Registra los "me gusta" que los usuarios dan a las publicaciones.';

-- PASO 8: Habilitar RLS y definir Políticas para likes
ALTER TABLE public.likes ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Likes are viewable by everyone."
ON public.likes FOR SELECT
USING (true);

CREATE POLICY "Authenticated users can like/unlike posts."
ON public.likes FOR INSERT
WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can delete their own likes (unlike)."
ON public.likes FOR DELETE
USING (auth.uid() = user_id);

----------------------------------------------------------------------------------------------

-- PASO 9: Creación de la tabla de seguidores (followers)
CREATE TABLE public.followers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  follower_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL, -- El que sigue
  following_id UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL, -- El que es seguido
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  -- Un usuario no puede seguir a otro más de una vez.
  UNIQUE(follower_id, following_id)
);

COMMENT ON TABLE public.followers IS 'Almacena las relaciones de seguimiento entre usuarios.';

-- PASO 10: Habilitar RLS y definir Políticas para followers
ALTER TABLE public.followers ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Follower relationships are viewable by everyone."
ON public.followers FOR SELECT
USING (true);

CREATE POLICY "Authenticated users can follow/unfollow others."
ON public.followers FOR INSERT
WITH CHECK (auth.uid() = follower_id);

CREATE POLICY "Users can delete their own follow relationships (unfollow)."
ON public.followers FOR DELETE
USING (auth.uid() = follower_id);

