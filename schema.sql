-- Venered Social - Supabase SQL Schema
-- Este script crea todas las tablas, relaciones y políticas de seguridad necesarias para la aplicación.

-- PASO 1: Creación de la tabla de perfiles (profiles)
-- Esta tabla almacena la información pública de los usuarios, extendiendo la tabla auth.users.
CREATE TABLE public.profiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  username TEXT UNIQUE NOT NULL,
  profile_pic_url TEXT,
  bio TEXT,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Comentarios sobre la tabla de perfiles
COMMENT ON TABLE public.profiles IS 'Almacena los perfiles públicos de los usuarios.';
COMMENT ON COLUMN public.profiles.id IS 'Referencia al ID del usuario en la tabla auth.users.';
COMMENT ON COLUMN public.profiles.username IS 'Nombre de usuario público y único.';

-- PASO 2: Habilitar la Seguridad a Nivel de Fila (RLS) para perfiles
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

-- PASO 3: Definir Políticas de Seguridad para perfiles
-- Política 1: Los perfiles son públicos y cualquiera puede verlos.
CREATE POLICY "Public profiles are viewable by everyone."
ON public.profiles FOR SELECT
USING (true);

-- Política 2: Los usuarios solo pueden crear un perfil para sí mismos.
CREATE POLICY "Users can insert their own profile."
ON public.profiles FOR INSERT
WITH CHECK (auth.uid() = id);

-- Política 3: Los usuarios solo pueden actualizar su propio perfil.
CREATE POLICY "Users can update own profile."
ON public.profiles FOR UPDATE
USING (auth.uid() = id);

-- PASO 4: Trigger para sincronizar nuevos usuarios
-- Esta función se ejecuta automáticamente cada vez que un nuevo usuario se registra en la aplicación (auth.users).
-- Crea una entrada correspondiente en la tabla public.profiles.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  -- Inserta una nueva fila en public.profiles con el id y un username.
  -- Si el username no se proporciona en los metadatos, se generará uno por defecto usando el ID del usuario.
  INSERT INTO public.profiles (id, username)
  VALUES (
    new.id,
    COALESCE(new.raw_user_meta_data->>'username', 'user-' || new.id::text)
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- El trigger se activa después de cada inserción en la tabla auth.users.
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

